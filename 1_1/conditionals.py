num = int(input("Enter a number -> "))
if (num >= 90):
  print("You got an A, good job!")
elif (num >= 80):
  print("You got a B, well done.")
elif (num >= 70):
  print("You got a C, you did alright.")
elif (num >= 60):
  print("You got a D, you passed!")
else:
  print("You have failed successfully.")
